    private Timer t;
    private int h, m, s;
    private ActionListener acciones = new ActionListener() { //clase anonima
        @Override
        public void actionPerformed(ActionEvent ae) {
            s++;
            if (s == 60) {
                s = 0;
                ++m;
            }
            if (m == 60) {
                m = 0;
                ++h;
            }
            actualizarLabel();
        }
    };
    
    private void actualizarLabel(){
        String tiempo = (h<=9?"0":"")+h+":"+(m<=9?"0":"")+m+":"+(s<=9?"0":"")+s; //apli operador terneario
        jLabelTempo01.setText(tiempo);
    }
    private void jButtonInicio02ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInicio02ActionPerformed
        t.start();
        jButtonInicio02.setEnabled(false);
        jButtonInicio02.setText("Reanudar");
        jButtonPausa02.setEnabled(true);
        jButtonDetener03.setEnabled(true);
    }//GEN-LAST:event_jButtonInicio02ActionPerformed

    private void jButtonPausa02ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPausa02ActionPerformed
        t.stop();
        jButtonInicio02.setEnabled(true);
        jButtonPausa02.setEnabled(false);
    }//GEN-LAST:event_jButtonPausa02ActionPerformed

    private void jButtonDetener03ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDetener03ActionPerformed
        if(t.isRunning())
        {
            t.stop();
            jButtonInicio02.setEnabled(true);
        }
        jButtonInicio02.setText("Iniciar");
        jButtonPausa02.setEnabled(false);
        jButtonDetener03.setEnabled(false);
        h=0; m=0; s=0; 
        actualizarLabel();
    }//GEN-LAST:event_jButtonDetener03ActionPerformed

