    private Timer t;
    private int h, m, s;
    private ActionListener acciones = new ActionListener() { //clase anonima
        @Override
        public void actionPerformed(ActionEvent ae) {
            s++;
            if (s == 60) {
                s = 0;
                ++m;
            }
            if (m == 60) {
                m = 0;
                ++h;
            }
            actualizarLabel();
        }
    };
    
    private void actualizarLabel(){
        String tiempo = (h<=9?"0":"")+h+":"+(m<=9?"0":"")+m+":"+(s<=9?"0":"")+s; //apli operador terneario
        jLabelTempo01.setText(tiempo);
    }
    private void jButtonInicio01ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInicio01ActionPerformed
        t.start();
        jButtonInicio01.setEnabled(false);
        jButtonInicio01.setText("Reanudar");
        jButtonPausa01.setEnabled(true);
        jButtonDetener01.setEnabled(true);
    }//GEN-LAST:event_jButtonInicio01ActionPerformed

    private void jButtonPausa01ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPausa01ActionPerformed
        t.stop();
        jButtonInicio01.setEnabled(true);
        jButtonPausa01.setEnabled(false);
    }//GEN-LAST:event_jButtonPausa01ActionPerformed

    private void jButtonDetener01ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDetener01ActionPerformed
        if(t.isRunning())
        {
            t.stop();
            jButtonInicio01.setEnabled(true);
        }
        jButtonInicio01.setText("Iniciar");
        jButtonPausa01.setEnabled(false);
        jButtonDetener01.setEnabled(false);
        h=0; m=0; s=0; 
        actualizarLabel();
    }//GEN-LAST:event_jButtonDetener01ActionPerformed

